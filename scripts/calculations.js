const addition = (total,num) => {
    return total+num;
}

const substraction = (total,num) => {
    return total-num;
}

const multiplication = (total,num) => {
    return total*num;
}

const division = (total,num) => {
    return total/num;
}

const clearInput = () => {
    let element = document.getElementById('inputField');
    element.innerHTML = null;
}


